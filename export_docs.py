# 版本目录基础路径
BASE_DIR = '/home/zhang/.copaw/news_system/版本历史'

def export_docs():
    """导出所有文档到版本目录"""
    for version_id, docs in VERSIONS.items():
        version_dir = os.path.join(BASE_DIR, version_id)
        
        # 确保目录存在
        if not os.path.exists(version_dir):
            os.makedirs(version_dir)
            print(f"创建目录: {version_dir}")
        
        # 导出每个文档
        for doc_type, filename in DOC_TYPE_MAP.items():
            if doc_type in docs:
                content = docs[doc_type]
                filepath = os.path.join(version_dir, filename)
                with open(filepath, 'w', encoding='utf-8') as f:
                    f.write(content)
                print(f"导出: {version_id}/{filename}")

if __name__ == '__main__':
    export_docs()
    print("\n导出完成！")
